# Addition
print("3 + 4 = ", 3 + 4)

# Multiplication
print("3 * 20= ", 3 * 20)

# Division
print("12 / 4= ", 12/4)

# Mixed Operation(PEMDAS)
print("8 + 2 * 10 = ", 8 + 2 * 10)
print("(8 + 2) * 10 = ", (8 + 2)*10)

# Integer division(drops the decimal part)
print("18 // 4 = ", 18//4)

# Modulus operator(%)
print("18 % 4= ", 18%4)

# Repeated Multiplication
print("5 * 5 * 5 = ", 5 * 5 * 5)

# Exponentiation
print("5 ** 3 = ", 5**3)

# Variables are placeholders. They start with a letter and can contain numbers in their
# names.
tuna = 5
print("tuna = ", tuna)
print("20 + tuna = ", 20 + tuna)
bacon = 18
print("bacon = ", bacon)
print("bacon / tuna = ", bacon/tuna)
